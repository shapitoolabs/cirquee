export const consoleLog = (text: string) => {
  console.log('💬', text);
};

export const consoleError = (text: string) => {
  console.error('⚠️', text);
};

export const consoleWarn = (text: string) => {
  console.warn('🚨', text);
};

export const consoleInfo = (text: string) => {
  console.info('ℹ️', text);
};

export const consoleDebug = (text: string) => {
  console.debug('🐛', text);
};

export const consoleSuccess = (text: string) => {
  console.log('✅', text);
};
